        printf("%2dÂº|%-14s| %2d | %2d | %2d | %2d |%3d |%3d |%3d | %c%c%c%c%c |\n",k+1,nome[Posicao[i]],Pontos[i],v[i],e[i],d[i],gp[i],gc[i],sg[i],Forma[i][0],Forma[i][1],Forma[i][2],Forma[i][3],Forma[i][4]);
